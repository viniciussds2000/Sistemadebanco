from conta import Conta
class Cartao:
    def __init__(self,numero,codigo,bandeira):
        numero.self = numero
        codigo.self = codigo
        bandeira.self = bandeira
    class Debito:
        def __init__(self,debito):
            Cartao.__init__(self,numero,codigo,bandeira)
            debito.self = debito
    class Credito:
        def __init__(self,credito,saldo):
            Cartao.__init__(self,numero,codigo,bandeira)
            credito.self = credito
            saldo.self = saldo




